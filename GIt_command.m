%% =========================================================================
%% Git 常用命令速查表
%% =========================================================================

%% =========================================================================
%% 0. 提交代码（Add → Commit → Push）
%% =========================================================================

!git add .
% 添加所有修改到暂存区

msg = ['update ', datestr(now,'yyyymmdd HH.MM')];
system(['git commit -m "', msg, '"']);
% 提交修改

!git push
% 推送当前分支到远程仓库

!git push -u origin branch_1
% 首次推送并建立远程跟踪关系


%% =========================================================================
%% 1. 分支管理（查看 / 创建 / 切换 / 重命名）
%% =========================================================================

!git branch
% 查看本地分支（当前分支带 *）

!git branch -a
% 查看所有分支（本地 + 远程）

!git switch main
% 切换到 main 分支

!git switch codex/convert-myproblem-to-problem-class-for-platemo
% 切换到指定分支

!git switch -c branch_4
% 创建并切换到新分支

!git branch -m old_name new_name
% 重命名分支


%% =========================================================================
%% 2. 分支删除（本地 / 远程）
%% =========================================================================

!git branch -d branch_4
% 删除本地分支（仅允许删除已合并分支）

!git branch -D branch_4
% 强制删除本地分支

!git push origin --delete branch_4
% 删除远程分支


%% =========================================================================
%% 3. 查看仓库状态（高频使用）
%% =========================================================================

!git status
% 查看当前工作区状态

!git branch
% 查看当前所在分支

!git log -1
% 查看最近一次提交

!git log --oneline --graph --all
% 图形化查看提交历史

!git diff
% 查看未提交修改


%% =========================================================================
%% 4. 更新代码（Fetch / Pull）
%% =========================================================================

!git fetch
% 获取远程更新，但不合并

!git pull
% 拉取并合并当前跟踪分支

!git pull origin main
% 拉取指定分支


%% =========================================================================
%% 5. 撤销与恢复（危险操作）
%% =========================================================================

!git restore .
% 撤销工作区所有未暂存修改（推荐）

!git checkout .
% 旧版写法

!git reset --hard
% 回退到最近一次提交

!git reset --hard HEAD~1
% 回退到上一次提交

!git clean -fd
% 删除未跟踪文件和目录


%% =========================================================================
%% 6. 远程仓库信息
%% =========================================================================

!git remote -v
% 查看远程仓库地址

!git remote show origin
% 查看 origin 详细信息


%% =========================================================================
%% 7. 推荐工作流
%% =========================================================================

% 查看状态
!git status

% 提交代码
!git add .
msg = ['update ', datestr(now,'yyyymmdd HH.MM')];
system(['git commit -m "', msg, '"']);

% 推送
!git push

% 更新远程代码
!git pull