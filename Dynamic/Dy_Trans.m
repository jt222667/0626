function [rc_ref, I_ref] = Dy_Trans(~, rc_local, I_local, R, P)
%TRANS  将质心位置和质心惯量张量从刚体局部坐标系转换到参考坐标系。
%
%   [rc_ref, I_ref] = TRANS(~, rc_local, I_local, R, P)
%
%   输入
%   ----- 
%   ~        : 保留的质量参数，兼容旧调用接口
%   rc_local : 质心在局部坐标系 Σ_b 中的 3×1 向量
%   I_local  : 质心惯量张量（3×3），关于刚体自身质心且表达在 Σ_b
%   R        : 方向余弦矩阵，Σ_b → Σ_ref
%   P        : 位移向量，Σ_ref 原点 → Σ_b 原点，在 Σ_ref 中表达
%
%   输出
%   -----
%   rc_ref : 质心在参考坐标系 Σ_ref 中的 3×1 向量
%   I_ref  : 质心惯量张量（3×3），关于刚体自身质心且表达在 Σ_ref
%
%   公式依据：质心坐标变换 + 惯量张量姿态变换
%
%   作者：<你的名字>  日期：<填写日期>
% -------------------------------------------------------------------------

    % (1) 质心坐标变换
    rc_ref = R * rc_local + P;

    % (2) 输入惯量已经是质心惯量，这里只做坐标姿态变换
    I_ref = R * I_local * R.';
    I_ref = (I_ref + I_ref.') / 2;
end
